export function assignImg(type)
{
  if (type == "Flood")
    return "/assets/images/Flood.jpg";
  if (type == "Cyclone")
    return "/assets/images/Cyclone.png";
  if (type == "Earthquake")
    return "/assets/images/Earthquake.jpg";
  if (type == "Landslide")
    return "/assets/images/Landslide.jpg";
  if (type == "Drought")
    return "/assets/images/Drought.jpg";
  if (type == "Fire")
    return "/assets/images/Fire.jpg";
}